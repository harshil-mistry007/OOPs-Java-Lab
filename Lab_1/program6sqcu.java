public class program6sqcu {
    public static void main(String[] args) {

        System.out.print("square" + "\t");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i * i + "\t");
        }
        System.out.print("\n");
        System.out.print("cube" + "\t");

        for (int i = 0; i <= 10; i++) {
            System.out.print(i * i * i + "\t");
        }
    }

}
